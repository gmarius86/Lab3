package lab2;
public class Point 
{
	private int x;
	private int y;
	
	// constructor
	//------------
	public Point (int xx, int yy)
	{
		this.x = xx;
		this.y = yy;
	}
	// publi functions
	//----------------s
	public int getX() {return x;}
	public int getY() {return y;}
	public void setX(int xx) {x = xx;}
	public void setY(int yy) {y = yy;} 
	//Private functions
	//-----------------
}
